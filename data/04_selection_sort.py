#
# Isola (seleciona) o primeiro elemento da lista e, em seguida,
# encontra o menor valor restante da lista. Se o valor encontrado
# for menor que o selecionado, efetua troca.
# Em seguida, isola o segundo número da lista, buscando pelo menor
# valor das posições subsequentes. Faz a troca entre dois valores,
# se necessário. Continua o processo, até isolar o penultimo elemento 
# da lista